// variables: let, var, const

const name = "Javascript";
let isAssignmentCompleted = false;
let number = null;
var one;

console.log(typeof name, name); // Data Type: string Value: Javascript

console.log(typeof isAssignmentCompleted, isAssignmentCompleted); // Data Type: boolean Value: false

console.log(typeof number, number); // Data Type: object Value: null

console.log(typeof one, one); // Data Type: undefined Value: undefined
